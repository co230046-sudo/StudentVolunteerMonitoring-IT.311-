<script>
document.addEventListener("DOMContentLoaded", () => {

    const shapes = Array.from(document.querySelectorAll("#regSVG g[id^='regShape']"));
    const form = document.querySelector(".reg-form");

    const idleRange = 55;      // stronger idle motion
    const idleMin = 3500;
    const idleMax = 6500;

    const rand = (min, max) => Math.random() * (max - min) + min;

    shapes.forEach((shape, index) => {

        const box = shape.getBBox();
        const ox = box.x;
        const oy = box.y;

        const centerX = window.innerWidth / 2 - box.width / 2;
        const centerY = window.innerHeight / 2 - box.height / 2;

        // Start in middle
        shape.style.transform = `translate(${centerX - ox}px, ${centerY - oy}px)`;

        const angle = (index / shapes.length) * Math.PI * 2;
        const burstDist = 260 + Math.random() * 200;

        const burstX = Math.cos(angle) * burstDist;
        const burstY = Math.sin(angle) * burstDist;

        // -------------------------
        // BURST OUT
        // -------------------------
        shape.animate(
            [
                { transform: `translate(${centerX - ox}px, ${centerY - oy}px)` },
                { transform: `translate(${centerX - ox + burstX}px, ${centerY - oy + burstY}px)` }
            ],
            {
                duration: 950,
                easing: "cubic-bezier(.65, 0, .35, 1)",
                fill: "forwards"
            }
        );

        // -------------------------
        // RETURN HOME
        // -------------------------
        setTimeout(() => {
            shape.animate(
                [
                    { transform: `translate(${centerX - ox + burstX}px, ${centerY - oy + burstY}px)` },
                    { transform: "translate(0px, 0px)" }
                ],
                {
                    duration: 1350,
                    easing: "cubic-bezier(.30, 0, .20, 1)",
                    fill: "forwards"
                }
            );
        }, 950);


        // -------------------------
        // IDLE FLOAT FOREVER
        // -------------------------
        setTimeout(() => {

            let posX = 0;
            let posY = 0;

            function idleFloat() {

                // Large smooth drift each time
                const nextX = posX + rand(-idleRange, idleRange);
                const nextY = posY + rand(-idleRange, idleRange);

                const duration = rand(idleMin, idleMax);

                shape.animate(
                    [
                        { transform: `translate(${posX}px, ${posY}px)` },
                        { transform: `translate(${nextX}px, ${nextY}px)` }
                    ],
                    {
                        duration,
                        easing: "cubic-bezier(.46, .03, .52, .96)", // liquid-like
                        fill: "forwards"
                    }
                ).onfinish = () => {
                    posX = nextX;
                    posY = nextY;
                    idleFloat();
                };
            }

            idleFloat();

        }, 2300);

    });


    // -------------------------
    // FORM SLIDE FADE-IN
    // -------------------------
    setTimeout(() => {
        if (!form) return;

        form.animate(
            [
                { opacity: 0, transform: "translateX(120px)" },
                { opacity: 1, transform: "translateX(0px)" }
            ],
            {
                duration: 950,
                easing: "cubic-bezier(.25, .8, .25, 1)",
                fill: "forwards"
            }
        );
    }, 1700);

});
