<script>
document.addEventListener("DOMContentLoaded", () => {

    const shapes = ["shape1", "shape2", "shape3", "shape4", "shape5"].map(id => document.getElementById(id));
    const form = document.querySelector(".form-slide");

    // Idle motion configuration
    const idleRange = 55;          // stronger organic drifting
    const idleMin = 3500;
    const idleMax = 6500;

    const rand = (min, max) => Math.random() * (max - min) + min;

    shapes.forEach((el, index) => {
        if (!el) return;

        const original = el.getBBox();
        const ox = original.x;
        const oy = original.y;

        // Center burst origin
        const centerX = window.innerWidth / 2 - original.width / 2;
        const centerY = window.innerHeight / 2 - original.height / 2;

        // Initially move shape into center
        el.style.transform = `translate(${centerX - ox}px, ${centerY - oy}px)`;

        // Burst direction + distance
        const angle = (index / shapes.length) * Math.PI * 2;
        const burstDist = 260 + Math.random() * 200;
        const burstX = Math.cos(angle) * burstDist;
        const burstY = Math.sin(angle) * burstDist;

        // --------------------------------------
        // BURST OUT
        // --------------------------------------
        el.animate(
            [
                { transform: `translate(${centerX - ox}px, ${centerY - oy}px)` },
                { transform: `translate(${centerX - ox + burstX}px, ${centerY - oy + burstY}px)` }
            ],
            {
                duration: 950,
                easing: "cubic-bezier(.65, 0, .35, 1)",
                fill: "forwards"
            }
        );

        // --------------------------------------
        // SMOOTH RETURN HOME
        // --------------------------------------
        setTimeout(() => {
            el.animate(
                [
                    { transform: `translate(${centerX - ox + burstX}px, ${centerY - oy + burstY}px)` },
                    { transform: "translate(0, 0)" }
                ],
                {
                    duration: 1350,
                    easing: "cubic-bezier(.30, 0, .20, 1)",
                    fill: "forwards"
                }
            );
        }, 950);

        // --------------------------------------
        // FLUID IDLE FLOATING (FOREVER)
        // --------------------------------------
        setTimeout(() => {

            let posX = 0;
            let posY = 0;

            function idleFloat() {

                const nextX = posX + rand(-idleRange, idleRange);
                const nextY = posY + rand(-idleRange, idleRange);

                const duration = rand(idleMin, idleMax);

                el.animate(
                    [
                        { transform: `translate(${posX}px, ${posY}px)` },
                        { transform: `translate(${nextX}px, ${nextY}px)` }
                    ],
                    {
                        duration,
                        easing: "cubic-bezier(.46, .03, .52, .96)", // fluid curve
                        fill: "forwards"
                    }
                ).onfinish = () => {
                    posX = nextX;
                    posY = nextY;
                    idleFloat();
                };
            }

            idleFloat();

        }, 2200);

    });

    // --------------------------------------
    // FORM SLIDE FADE-IN
    // --------------------------------------
    setTimeout(() => {
        if (!form) return;

        form.animate(
            [
                { opacity: 0, transform: "translateX(120px)" },
                { opacity: 1, transform: "translateX(0)" }
            ],
            {
                duration: 900,
                easing: "cubic-bezier(.25,.8,.25,1)",
                fill: "forwards"
            }
        );
    }, 1500);

});
</script>
